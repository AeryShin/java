public class OperatorEx20 {
    public static void main(String[] args) {
        System.out.println(-10%8);          // -2
        System.out.println(10%-8);          // 2
        System.out.println(-10%-8);         // 2

        /*
        % : 나머지 연산자
        나머지를 구하고, 부호는 왼쪽 (처음) 피연산자를 따라감.
         */
    }
}
