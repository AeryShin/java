public class FlowEx12 {
    public static void main(String[] args) {
        for(int i=1;i<=5;i++)
            System.out.println(i);              // 1 2 3 4 5 (줄바꿈)

        for(int i=1;i<=5;i++)
            System.out.print(i);                // 1 2 3 4 5 (줄바꿈 x)

        System.out.println();
    }
}
